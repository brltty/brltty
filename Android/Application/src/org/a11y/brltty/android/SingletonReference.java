package org.a11y.brltty.android;

public abstract class SingletonReference<T> {
  private T reference = null;

  protected abstract T onNeedReference ();

  public final T get () {
    synchronized (this) {
      if (reference == null) {
        reference = onNeedReference();
      }
    }

    return reference;
  }

  SingletonReference () {
  }
}
